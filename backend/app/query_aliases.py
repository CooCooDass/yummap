CATEGORY_ALIASES = {
    alias: category
    for category, aliases in CATEGORY_SYNONYMS.items()
    for alias in [category, *aliases]
}


def normalize_query_text(text: str) -> str:
    normalized = text.lower()
    for alias, category in sorted(CATEGORY_ALIASES.items(), key=lambda item: len(item[0]), reverse=True):
        normalized = normalized.replace(alias.lower(), category.lower())
    return normalized


def query_tokens(text: str) -> list[str]:
    tokens: set[str] = set()
    for raw_token in re.split(r"[\s,./·|]+", text.lower()):
        token = raw_token.strip()
        if len(token) < 2:
            continue
        tokens.add(token)
        canonical = CATEGORY_ALIASES.get(token)
        if canonical:
            tokens.add(canonical.lower())
        for suffix in ("집", "식당", "맛집", "요리", "전문점", "카페"):
            if token.endswith(suffix) and len(token) > len(suffix) + 1:
                stripped = token[: -len(suffix)]
                tokens.add(stripped)
                tokens.add(CATEGORY_ALIASES.get(stripped, stripped).lower())
    return list(tokens)
